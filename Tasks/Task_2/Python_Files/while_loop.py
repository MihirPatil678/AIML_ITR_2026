# Initialize The Counter Variable To 1
number = 1

# The While Loop Runs As Long As Number Is 20 Or Less
while number <= 20:

    # Cube Of The Current Number
    cube = number ** 3

    # Print The Number And Its Cube Side By Side
    print("Number:", number, "Cube:", cube)

    # Increment The Counter By 1 To Move To The Next Number
    number = number + 1
