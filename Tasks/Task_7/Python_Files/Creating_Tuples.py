# Tuple With 5 Numbers Using Parentheses
t1 = (10, 20, 30, 40, 50)
print(t1) 
print(type(t1))

# Mixed Tuple
t2 = (42, "hello", 3.14, True)
print(t2) 
print(type(t2))

# Empty Tuple - Two Ways
t3 = ()
t4 = tuple()
print(t3) 
print(type(t3))
print(t4) 
print(type(t4))

# Single Element Tuple
# Rule: A Comma Is Required To Make It A Tuple.
# (99) Is Just An Int; (99,) Is A Tuple.
t5 = (99,)
print(t5) 
print(type(t5))