# Given List & Variable
fruits = ["mango", "apple", "banana", "cherry"]
score = 50

# Add 25 To Score Using The += Assignment Operator
score += 25  # score = score + 25
print("Updated Score After Adding 25:", score)

# Check If "apple" Is IN The Fruits List
has_apple = "apple" in fruits
print("Is 'apple' IN The Fruits List:", has_apple)

# Check If "grape" Is NOT IN The Fruits List
no_grape = "grape" not in fruits
print("Is 'grape' NOT IN The Fruits List:", no_grape)