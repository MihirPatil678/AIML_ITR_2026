# Storing Information In Variables
full_name = "Mihir Sopan Patil"     # stores a person's full name(String)
age = 18                            # stores age in whole numbers(Integer)
height_meter = 1.7                  # stores height in meters(Float)
student = True                      # stores student True/False information(Boolean)

# Printing The Data Stored In Variables
print("Full Name:", full_name)
print("Age:", age)
print("Height (In Meters):", height_meter)
print("Is A Student:", student)

# Checking Data Types Of Variables
print("\nfull_name:", type(full_name))
print("age:", type(age))
print("height_meter:", type(height_meter))
print("student:", type(student))