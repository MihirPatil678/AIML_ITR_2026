# Given Values
x = 25
y = 30
z = 25

# Is x Greater Than y?
print("Is x Greater Than y?", x > y)  # False
# Is x Equal To z?
print("Is x Equal To z?", x == z)  # True
# Is x Less Than Or Equal To y AND y Is Not Equal To z?
print("Is x Less Than Or Equal To y AND y Is Not Equal To z?", x <= y and y != z)  # True
# Is x Greater Than y OR x Equal To z?
print("Is x Greater Than y OR x Equal To z?", x > y or x == z)  # True