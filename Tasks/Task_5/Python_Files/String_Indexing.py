user_string = input("Enter A String:")
 
# Positive indexing: Starts At 0 From The Left (0, 1, 2, ...)
print("First Character:", user_string[0])
 
# Negative indexing: Starts At -1 From The Right (-1, -2, -3, ...)
print("Last Character:", user_string[-1])
 
# Index 2 Gives The 3rd Character From The Start
print("3rd Character From Start:", user_string[2])
 
# Index -2 Gives The 2nd Character From The End
print("2nd Character From End:", user_string[-2])