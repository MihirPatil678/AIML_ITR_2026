student = {
    "name": "Mihir",
    "age": 18,
    "city": "Pune",
    "marks": 84
}
 
# Accessing A Value Using Square Brackets
print("Name:", student["name"])
 
# Updating An Existing Key's Value
student["marks"] = 92
 
# Adding A New Key-Value Pair
student["grade"] = "A"
 
print("Updated Student Dictionary:")
print(student)
