# A List Of 5 Integers Created Directly Using Square Brackets
integers = [10, 20, 30, 40, 50]
print("List Of 5 Integers:", integers)

# Empty List Using [] & list()
empty_list_1 = []      
empty_list_2 = list()   
print("Empty List Using []:", empty_list_1)
print("Empty List Using list():", empty_list_2)

# A List With Mixed Data Types
mixed = [42, "hello", 3.14, True]
print("List With Mixed Data Types:", mixed)

# A List Of 7 Zeros Using Repetition Operator (*)
zeros = [0] * 7
print("List Of 7 Zeros:", zeros)